                            Çocuklardaki Dil ve Konuşma Bozukluklarına Yönelik Tutumları</h4><br><br>

                            Çocuklardaki iletişim sorunları aile bildirimleri, öğretmen raporları ve dil ve konuşma
                            terapistleri (DKT) tarafından yapılan değerlendirmeler ile saptanmaktadır. Dil ve konuşma
                            bozukluklarının önlenmesi ve terapisinde iş birliği içinde bulunmaları gereken bu üçlü
                            grubun konuya ilişkin görüş ve tutumları alanyazında önemli bulunmaktadır.
                            Örneğin, dil ve konuşma terapistleri (DKT) anne babalarla sınırları belli bir iş
                            birliğinden daha katılımcı bir iş birliğine geçiş yapmışlardır. Küçük çocuklara uygulanan
                            müdahale yaklaşımlarında tercih edilen geleneksel ‘terapist merkezli modelde uzman kendini
                            odağa alıp hizmet alanlara daha yönlendirici ve kontrollü bir hizmet planlaması
                            uygularken, son zamanlarda benimsenen ‘aile merkezli model’de müdahalenin felsefesi
                            çocuğun ailesi ve terapisti arasında olumlu bir ilişki biçimlendirmek şeklinde olmuştur
                            <span class="dots">...</span><br><br>
                            <span class="more">

                            Terapistler anne babaları terapiye katmakla kalmayıp onlara karar mekanizmasında rol alma
                            olanağı da tanımaktadırlar (Pappas, McLeod, McAllister ve McKinnon, 2008).
                            Alanyazında, ailelerin, çocuklarının sağlığını ve gelişimini izleme sürecine katılımı
                            giderek daha çok önerilmekte ve sorunların erken tanılanması ve kendi rollerinin
                            belirlenmesi konusunda görüşlerinin alınmasının gerekliliği vurgulanmaktadır
                            (Hall ve Elliman, 2003). Dil ve konuşma sorunlu çocuklara yönelik değerlendirme ve terapi
                            hizmetlerinde kullanılan ölçümlerin yerini giderek aileden alınan bilgilere bıraktığı da
                            görülmektedir (Glogowska ve Campbell, 2000). <br><br>
                            Terapi sürecinde aileye tanınan sorumluluk artışına karşın, ailelerin bu sürece
                            katılımlarıyla ilgili görüşlerini belirlemeye yönelik araştırmalarda (Ramig, 1993) çok
                            sayıda ailenin tarama, değerlendirme ve terapiye katılım için yüksek motivasyona sahip
                            olduğu ama pek çoğunun da bu rolü yerine getiremedikleri belirtilmiştir (Law, 2000).
                            Katılımda sınırlı kalsalar bile, ailelerin çocuklarının dil ve konuşma terapilerine
                            katılması ve terapilerin kalitesi hakkındaki yorumları oldukça olumludur; ek olarak,
                            her ailenin çocuğu için daha fazla terapi seansı istediği belirtilmektedir. Sınıf
                            öğretmenlerine de dil ve konuşma bozukluklarına müdahale programlarında konuşma
                            terapistlerine eşlik etme rolünün önerilmesi son yıllarda önem kazanan bir konu olmuştur.
                            Çalışmalar, öğretmenlerin dil ve konuşma bozukluğu olan
                            çocukları konunun uzmanı ile birlikte tanılaması ve terapi önermesinin önemini vurgular
                            niteliktedir (Russell ve Kaderavek, 1993; Reid ve diğ., 1996; Hartas, 2004; Overby,
                            Carrell ve Bernthal, 2007). Özellikle kaynaştırma sınıflarında, çoğu dil ve konuşma
                            terapistinin, geleneksel biçimde, çocukla sınıf dışında yalnız çalışmasının öğretmenlerin
                            sürece katılmada ve çocukların iletişim sorunları ile ilgili gereksinimlerini karşılamada
                            çok az sorumluluk almalarına yol açtığını belirtmektedir (Reid ve diğ., 1996). Öğretmenler
                            öğrencilerin okuldaki akademik, sosyal ve davranışsal durumlarına göre terapiye alınmaları
                            konusunda birincil olarak danışılan kişilerdir; bu da öğretmenin çocuğun sorununa ne
                            derece olumlu bakış açısı geliştirdiğine bağlıdır (Overby ve diğ., 2007). Çocuğun durumu
                            hakkında yapılabilecek yanlış tanılamalara yol açan engellerden en önemlisi öğretmenler ve
                            dil ve konuşma terapistleri <br><br>

                            Arasında ki olası tutum farklılıklarıdır. Dil çocukların eğitim yılları boyunca
                            okuryazarlığın gelişiminde çok temeldir. Tüm çocuklar için, özellikle sözlü dil,
                            iletişimsel, akademik, sosyal ve duygusal gelişimleri için zorunlu olup, okuma ve yazma
                            becerilerinin gelişimini destekler. Okul öncesinde dil ve konuşma yetilerinde bir
                            bozukluk/yetersizlik yaşayan ve/veya değişen nedenlerle daha önceden sorunları tanılamadan
                            okula başlamış çocukların okuma ve yazmada zorluk yaşayabilme riski bulunmaktadır. <br><br>

