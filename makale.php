                            <h4 class="text-center font-weight-bold title py-3 ">DİL GELİŞİMİNE ETKİ EDEN FAKTÖRLER
                            </h4><br><br>
                            <b>Öğrenme ve Olgunlaşma: </b>Genel olarak olgunlaşma ve öğrenme ile ilgili öğeler çocuğun
                            ilk dil gelişiminde önemli rol oynarlar. Çocuğun dili akıcı bir şekilde kullanılabilir bir
                            hale gelmesi için belirli bir olgunluk düzeyine gelmesi ve nitelikli bir öğrenme sürecinden
                            geçmesi gerekmektedir
                            <span class="dots">...</span><br><br>
                            <span class="more">

                            <b>Sağlık:</b> Konuşma insan bedenindeki ses organları aracılığı ile seslerin oluşturulup
                            çıkarılması olup larenks ve ses telleri aracılığı ile gerçekleşen fonasyon ve ağız yapıları
                            (dil, damak, dudak ve dişler) ile sağlanan artikülasyon olmak üzere iki temel bileşeni
                            vardır. Bu tür fizyolojik koşulların uygun olması dil gelişimi yönünden önemlidir. İşitme
                            algısının normal olması sağlıklı dil gelişimi yönünden önemlidir. Duyma kusuru olan
                            bebeklerin 4-8 aylar arasında yapılan ses oyunları döneminde normalden ayrıldığı, daha
                            az sessiz harf kullandıkları ve 4-18 aylar içinde ses üretiminde ki yaratıcılığın giderek
                            azaldığı dikkatin, ses oyunlarından işitsel geri alım olmadığı için bebeğin dil gelişiminin
                            aksadığı belirtilmiştir Görsel algılama dil gelişimi için belirleyici olmakta, ciddi
                            görme kaybı olan çocukların dil gelişimleri, görmesi normal olanlara göre daha geç
                            başlamaktadır. Şiddetli ve uzun süren rahatsızlıklar çocuğun konuşmasını olumsuz
                            etkilemekte, gecikmelere neden olmaktadır.<br><br>

                            <b>Psikolojik sağlık:</b> Çocuğun psikolojik açıdan sağlıklı bir çevrede büyümemesi, eşler
                            arasındaki sorunlar, kardeşler arası karşılaştırmalar ve çocuğun duygusal ihtiyaçlarının
                            tam olarak doyurulmaması durumunda konuşmada gecikmeler ve bozukluklar
                            görülebilmektedir.<br><br>

                            <b>Sosyo-ekonomik durum:</b> Çeşitli araştırmalar yüksek sosyo-ekonomik düzeydeki
                            ailelerden gelen çocukların fakir ailelere kıyasla cümle uzunluğu, soru sayısı, kelime
                            haznesi bakımından daha ilerde olduklarını göstermiştir. Çocuğun yaşıtlarının da
                            bulunduğu oyun ve eğitim gruplarına katılımı, kitap okuma, gezip görme ve düşüncelerini
                            söylemeyi teşvik yönünde sağlanan fırsatların sosyo-kültürel ve sosyo-ekonomik durumu
                            iyi olan ailelerde daha fazla olması bu ailelerde yetişen çocukların daha erken ve düzgün
                            konuşması ile ilişkilendirilmiştir.<br><br>

                            <b>Konuşmaya teşvik:</b> Dil kazanımı temelde aynı sırayı izlese de bu gelişimin hızı sosyal
                            çevreden etkilenmektedir. Erişkinlerin bebekle erken dönemde kurdukları sözel iletişim
                            bebeğin anadilinin öğrenmesinin temelini oluşturur. Çevre ve özellikle anne tarafın
                            çocuğa sunulan sözel uyaran zenginliğinin dil gelişimini olumlu etkileyeceği
                            bildirilmektedir. Kendileri ile konuşulan ve ilgi gösteren çocuklar konuşma için
                            cesaretlendirilirler. Ebeveynler doğduklarından itibaren bebekle konuşarak bebeği, dil
                            kapasitesinin ortaya çıkması için uyarmalıdırlar. Okul öncesi çocuğa kitap okunduğunda,
                            TV izlemesine izin verildiğinde ve oyun gruplarına sokulduklarında konuşmak için daha
                            çok cesaretlenmekte ve bu becerileri daha fazla artmaktadır. <br> <br>

                            <b>Bireysel farklılıklar</b> ve cinsiyet: Her çocuğun konuşma ile ilgili kapasitesi
                            diğerlerinden farklıdır. 12 aylık dönemde hemen hemen tüm çocuklar ilk sözcüklerini
                            söylerler. Ancak, bu çok değişken olabilir; kimileri bunu sekizinci ayda gerçekleştirirken,
                            kimileri de 18 aylık döneme kadar bunu gerçekleştirememiş olabilir. Dili kullanmayı öğrenmek
                            dereceli bir süreçtir. Aynı ailedeki çocukların konuşmayı öğrenme süreleri farklı olabilir.
                            Yapılan bazıaraştırmalara göre kız çocukları, erkek çocuklarına göre konuşma miktarı,
                            kelime çeşidi ve kullanılan gramer kurallarını öğrenmeyi daha erken kazanabilmektedirler.
                            <br><br>

                            <b>Aile bebek ilişkileri:</b> Bakım evlerinde büyüyen çocuklar aile içerisinde büyüyen
                            çocuklara oranla daha çok ağlarlar. Ancak daha az hecelerler. Ailedeki kişilerin
                            arasındaki ilişkiler ve ailenin bebekle iyi ilişkileri dil gelişiminde önemli bir etkendir.
                            Zihinsel özrü olan annelerin bebeklerinin, dil gelişimi yönünden risk altında olduğu
                            hipotezi ile bu grup erken dönemde ev eğitim programları ile desteklenmiş, anne-bebek
                            ile etkileşiminin artırılmasının dil gelişimine olumlu etkilediği belirtilmiştir.<br><br>

                            <b>Zeka:</b> Yapılan çeşitli araştırmalar dil yeteneği ile zeka arasında olumlu doğrusal
                            bir ilişki olduğunu göstermektedir. Erken konuşan çocukların zeka gelişimlerinin diğerlerine
                            oranla daha üstün olduğu ileri sürülmektedir.<br><br>

                            <b>Oyun:</b> Okul öncesi dönemdeki çocuğa sözcükler veya kavramlar öğretileceği zaman
                            oyun içerisinde öğretilmelidir. Oyun çocuğun duygusal, bedensel, psikomotor gelişimini
                            etkilerken aynı zamanda dil ve mantık gelişimini de etkilemektedir. Oyun sırasında
                            arkadaşlarından ya da yetişkinlerden fark etmeden yeni sözcükleri, nesneleri ve bunların
                            işlevlerini, kavramları öğrenir.<br><br>

                            <b>İki dillilik:</b> İki ayrı dilin konuşulduğu ortamlarda yaşayan ya da iki dil öğrenmek
                            zorunda kalan çocuklar başlangıçta tek dili öğrenen çocuklara göre daha yavaş bir gelişim
                            gösterirler. Küçük çocukların yabancı dilin ses ve duyuş özelliklerine karşı keskin bir
                            kulakları olduğu, daha ileri yaşlarda ise bireylerin dil öğrenirken daha çok dil bilgisi,
                            kavram ve anlam üstünde durdukları, bu nedenle ikinci bir dilin öğrenilmesinde küçük
                            çocukların daha üstün oldukları ileri sürülmektedir. Sonuç olarak tüm bebekler dili
                            öğrenmeye öncelikle kullanılan dildeki sesleri öğrenmekle başlarlar. Seslerden hecelere,
                            cümlelere ve dilin tam olarak anlaşılmasına dek süren süreç içerisinde sıra değişmezken,
                            gelişimin hızı tüm bu etmenlerden etkilenmektedir.<br><br>

