                            <h4 class="text-center font-weight-bold title py-3">DİL VE KONUŞMA BECERİLERİNİN
                            GELİŞİMİ VE ÖNEMİ</h4><br><br>

                            Dil, iletişim sağlama aracı olarak kullanılan, sesler, işaretler (semboller) ve sözcükler
                            gibi temel birimleri olan bir sistemdir (Baykoç, 1986: 90). Dil bir anlaşma aracıdır ve
                            temelde insanlar arası iletişimi sağlar. Bununla birlikte dil, yalnızca bir iletme aracı
                            olmakla kalmaz, iletimin bizzat kendisi haline gelerek, insana evreni açar
                            (Poyraz, 1995: 11). Dil, çocuğu egosundan uzaklaştırıp, onun sosyal bir kişi olmasını
